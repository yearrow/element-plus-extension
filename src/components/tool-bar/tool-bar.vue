
<template>
  <div class="yl-tool-bar">
    <div class="more">
      <div v-if="more" class="more-lable" @click="_display">
        <i class="el-icon-filesearch" /> {{ display }}
      </div>
      <div v-if="display == '隐藏'" class="more-content">
        <slot name="more" />
      </div>
    </div>
    <div class="filter">
      <slot name="filter" />
    </div>
    <div v-if="divider" class="divider" />
    <div v-if="tool" class="tool">
      <slot />
    </div>
  </div>
</template>

<script lang="ts" setup>
import { ref } from 'vue'
export interface Props {
  divider?: boolean
  more?: boolean
  tool?: boolean
}

defineOptions({
  name: 'YlToolBar',
  inheritAttrs: false,
})


withDefaults(defineProps<Props>(), {
  /**
   * 分割线
   */
  divider: true,
  /**
   * 展开更多
   */
  more: false,
  /**
   * 显示工具栏
   */
  tool: true
})
const display = ref('展开')

const _display = () => {
  if (display.value === '展开') {
    display.value = '隐藏'
  } else {
    display.value = '展开'
  }
}
</script>